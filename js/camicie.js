function renderPDP(productId) {
  const product = productsCamicie.find((item) => item.id == productId);

  // title
  document.getElementById("pdpTitle").innerText = product.title;

  // prices
  document.getElementById("singlePrice").innerText = product.singlePrice;

  document.getElementById("oldPrice").innerText = `4  x ${product.oldPrice}`;

  document.getElementById("discountBadge").innerText = product.discount;

  document.getElementById("finalPrice").innerText = product.finalPrice;

  // fabric
  document.getElementById("fabricText").innerText = product.pdp.fabric;

  // color
  document.getElementById("colorName").innerText = product.pdp.colorName;

  document.getElementById("colorCircle").style.background =
    product.pdp.colorCode;

  // main image
  document.getElementById("mainProductImage").src = product.pdp.gallery[0];

  // thumbs
  const thumbsWrapper = document.getElementById("thumbsWrapper");

  thumbsWrapper.innerHTML = "";

  product.pdp.gallery.forEach((img) => {
    thumbsWrapper.innerHTML += `

      <img
        src="${img}"
        class="thumb-img"
      />

    `;
  });

  // sizes
  const sizesWrapper = document.getElementById("sizesWrapper");

  sizesWrapper.innerHTML = "";

  product.pdp.sizes.forEach((item) => {
    sizesWrapper.innerHTML += `

      <button
        class="size-btn ${item.available ? "" : "disabled"}"
      >
        ${item.size}
      </button>

    `;
  });

  // desc
  document.getElementById("descriptionText").innerText =
    product.pdp.description;

  // washing
  document.getElementById("washingText").innerText = product.pdp.washing;

  // specs
  const specificationsList = document.getElementById("specificationsList");

  specificationsList.innerHTML = "";

  product.pdp.specifications.forEach((spec) => {
    specificationsList.innerHTML += `

      <li>${spec}</li>

    `;
  });

  // show pdp
  // document.querySelector(".products-sec").classList.add("d-none");

  document.getElementById("pdpPage").classList.remove("d-none");
}

document.addEventListener("click", (e) => {
  const card = e.target.closest(".product-card");

  if (!card) return;

  const productId = card.dataset.id;

  window.location.href = `product-detail.html?id=${productId}`;
});

const params = new URLSearchParams(window.location.search);

const productId = Number(params.get("id"));

if (productId) {
  renderPDP(productId);
}
